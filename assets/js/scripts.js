/*
1.generate a random number between 10 and 50
2. assign a random number to each crystal from 1 to 10
3. create a click function for when crystals are pressed and have the value of each crystal added to the score box below
4. if the numbers generated by crystals match the score box then count it as a win and if it exceeds count it as a loss
5. reset the random number and the users scroe at the end of the game or start of new one 


*/

function getRandomNumber(min, max) {
	var randomDecimal = Math.random() * (max - min);
	var randomWholeNum = Math.round(randomDecimal) + min;
	return randomWholeNum;
}

function randomCrystalPoints() {
	var crystalPoints = [];
	for (var i=0; i<4; i++) {
		var currentRandomNumber = getRandomNumber(1, 10);
		crystalPoints.push(currentRandomNumber);
	}

	return crystalPoints;
}

function game() {
	var $randomNumberBox = $(".randomNumber");
	var $wins = $("#wins");
	var $losses = $("#losses");
	var $crystal = $(".crystal");
	var $crystals = $(".crystals");
	var $resultBox = $(".resultBox");
	var randomNumber = getRandomNumber(10, 50);
	var score = 0;
	var numOfWins = 0;
	var numOfLosses = 0;
	var crystalPoints = randomCrystalPoints();
	$randomNumberBox.html(randomNumber);

	$crystals.on("click", ".crystal", function() {
		var currentItemId = Number($(this).attr("id"));
		score += crystalPoints[currentItemId];
		$(".resultBox").html(score);
		if (score === randomNumber) {
			numOfWins++;
			randomNumber = getRandomNumber(10, 50);
			crystalPoints = randomCrystalPoints();
			score = 0;
			$randomNumberBox.html(randomNumber);
			$resultBox.html(score);
			$wins.html(numOfWins);
		} else if (score > randomNumber) {
			numOfLosses++;
			randomNumber = getRandomNumber(10, 50);
			crystalPoints = randomCrystalPoints();
			score = 0;
			$randomNumberBox.html(randomNumber);
			$resultBox.html(score);
			$losses.html(numOfLosses);
		}
	});
}

game();







